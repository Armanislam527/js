// Declaring variables using var, let, and const
var name = "Alice"; // String
let age = 25; // Number
const country = "USA"; // Constant

// Reassigning variables
name = "Bob";
age = 26;

// Data types examples
let isStudent = true; // Boolean
let city = null; // Null
let score; // Undefined
let symbolKey = Symbol("unique"); // Symbol

// Arithmetic operation
let x = 10;
let y = 5;
let sum = x + y; // Addition
let product = x * y; // Multiplication

// Comparison and logical operators
let isEqual = x == y; // Comparison
let isGreater = x > y; // Comparison
let andCondition = x > 5 && y < 10; // Logical AND
let orCondition = x < 5 || y < 10; // Logical OR

// Output the results to the console
console.log("Name:", name);
console.log("Age:", age);
console.log("Country:", country);
console.log("Sum:", sum);
console.log("Product:", product);
console.log("Is Equal:", isEqual);
console.log("Is Greater:", isGreater);
console.log("AND Condition:", andCondition);
console.log("OR Condition:", orCondition);
